DROP FUNCTION IF EXISTS CantFicsPorUsuarioId;
DELIMITER //
CREATE FUNCTION CantFicsPorUsuarioId(id INT)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE cant INT;

    SELECT COUNT(*) INTO cant FROM Fics WHERE id_creador = id;

    return cant;
END//
DELIMITER ;


DROP FUNCTION IF EXISTS CantFicsPorA単o;
DELIMITER //
CREATE FUNCTION CantFicsPorA単o(a単o INT)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE cant INT;

    SELECT COUNT(*) INTO cant FROM `Fics` WHERE year(`time`) = a単o;

    return cant;
END//
DELIMITER ;
